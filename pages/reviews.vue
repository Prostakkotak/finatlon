                Я воспользовалась этой Олимпиадой при поступлении в вуз на то
                направление, которое хотела. Благодаря олимпиаде я не переживала
                о том, пройду ли я. Мне сейчас очень нравится процесс обучения,
                одногруппники, преподаватели. Также в РАНХИГС для тех, кто
                поступал по олимпиаде, выделено общежитие на территории
                университета, поэтому дорога до корпуса занимает максимум 5
                минут, да и само общежитие очень комфортное.
              </p>

                стала призёром этой олимпиады. Мне понравился формат проведения,
                когда всем участникам создали необходимые условия. Хоть финал и
                проводилось в несколько этапов, была зона с едой, так что
                ожидания между этапами было даже приятным. Организаторы на
                площадке были очень отзывчивыми, готовыми все показать и
                рассказать.<br />
                Отдельно хочется отметить сами этапы финала, где каждый смог
                найти себе задания по душе — и что-то вычислительное, и эссе, и
                возможность вживую поговорить об экономических темах.
              </p>
              <p>
                Когда я узнала результаты олимпиады, честно сказать, я плакала,
                потому что в неё было столько вложено и все труды оправдались.
              </p>

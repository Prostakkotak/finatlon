      школьникам в реализации способностей независимо от места проживания,
      обучения и социально-имущественного статуса родителей.
    </p>
    <p>
      <strong>Задача </strong>– использование Олимпиады для распространения
      финансовых знаний среди широкого круга учащихся, членов их семей, учителей
      (мультипликативный эффект).
    </p>
    <p>

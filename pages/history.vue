      учения, побуждающих учащихся к активной познавательной деятельности;
      развивает аналитические способности и креативность мышления; формирует
      знания, умения и навыки, необходимые для применения в других учебных
      дисциплинах, в трудовом процессе; позволяет приобрести каждым учеником
      веры в свои силы, уверенности в своих способностях и возможностях.
    </p>
